namespace AppBundle\Entity;

/**
 * Description of SelectRole
 *
 * @author wing
 */
class SelectRole {
    
    /**
     * @var string
     *
     */
    public $role;
    
    /**
     * Set role
     *
     * @param string $role
     * @return User
     */
    public function setRole($role)
    {
        $this->role = $role;

        return $this;
    }

    /**
     * Get role
     *
     * @return string 
     */
    public function getRole()
    {
        return $this->role;
    }
}
